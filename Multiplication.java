import java.util.*;

class Multiplication {
    public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);
    System.out.println("Enter Two Numbers");
    int a = sc.nextInt();
    int b = sc.nextInt();
    int multiply = a*b;
    System.out.println("Multiplication of" + a + " and " + b + "  is " + multiply);
    }
}